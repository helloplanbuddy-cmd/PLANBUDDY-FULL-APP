  '',
  '| File path | Line | Matched text | Category |',
  '|---|---:|---|---|'
)

Add-Content -Path $outPath -Value $header

# Also ensure log shows progress
"[STEP1] Enumerated production files under BACKEND/planbuddy_v9: $productionCount" | Tee-Object -FilePath $logPath -Append

$processed = 0
$filesWithMatches = 0
$totalMatches = 0

function EscapeMd([string]$s) {
  if ($null -eq $s) { return '' }
  # trim and collapse whitespace
  $t = ($s -replace "`r","" -replace "`n","" )
  $t = $t.Trim()
  if ($t.Length -gt 180) { $t = $t.Substring(0,180) + '…' }
  $t = $t -replace '\|','\\|'
  return $t
}

foreach ($f in $files) {
  $processed++
  if ($processed % 100 -eq 0) {
    "[STEP1] Processed $processed / $productionCount" | Tee-Object -FilePath $logPath -Append
  }

  $hasFileMatch = $false
  $lineNumber = 0

  try {
    # Stream line-by-line
    Get-Content -Path $f.FullName -Encoding UTF8 -ReadCount 1 | ForEach-Object {
      $line = $_
      $lineNumber++
      if ([string]::IsNullOrWhiteSpace($line)) { return }

      foreach ($p in $patterns) {
        # simple match where possible
        $cat = $p.Category
        $needle = $p.Match

        # substring match
        if ($line -like "*$needle*") {
          $hasFileMatch = $true
          $totalMatches++

          $row = "| $($f.FullName.Replace($repoRoot,'')).TrimStart('/\\') | $lineNumber | $(EscapeMd $line) | $cat |"
          Add-Content -Path $outPath -Value $row
          # flush by virtue of Add-Content
        }
      }
    }
  } catch {
    "[STEP1] WARN: Failed reading $($f.FullName): $($_.Exception.Message)" | Tee-Object -FilePath $logPath -Append
    continue
  }

  if ($hasFileMatch) { $filesWithMatches++ }
}

